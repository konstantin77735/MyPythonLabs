import math

a = 2.1
b = 6.7

# Первый случай: x < -2
x1 = -2.37
if x1 < -2:
    y1 = (x1 ** 3) + 2 * a

# Второй случай: -2 <= x <= 5
x2 = -0.49
if -2 <= x2 <= 5:
    y2 = math.log(abs(math.cos(b * x2)))

# Третий случай: x > 5
x3 = 7.51
if x3 > 5:
    y3 = (x3 ** 2) * math.exp(x3)

# Вывод результатов
print("Для x1 =", x1, ", y1 =", y1)
print("Для x2 =", x2, ", y2 =", y2)
print("Для x3 =", x3, ", y3 =", y3)
